import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, classification_report,confusion_matrix, roc_auc_score

"""# Data collection and processing"""

df = pd.read_csv("/content/sonar data (1).csv", header= None)

df.head(5)

df.tail()

df.info() #----> Basic Info

df.isnull().sum() #---> checking count of null

df.describe() #----> #Statical Measure

df[60].value_counts() #------> number of rows and columns

df.groupby(60).mean()

"""split x and y"""

x = df.drop (60, axis = 1)
y = df[60]

"""Train test split"""

x_train, x_test,y_train,y_test = train_test_split(x,y,test_size = 0.2, stratify = y, random_state = 42)

"""model"""

model = LogisticRegression()

model.fit(x_train, y_train)

y.pred = model.predict(x_test)

print(classification_report (y_test, y.pred))

print (confusion_matrix(y_test, y.pred))

model.score(x_test, y_test)

model.score(x_train, y_train)

